const express = require("express");

/////////////////////////////////////////////////////////////////////////////////////////////

const personalTourController = require('./../controllers/personalTourController');

/////////////////////////////////////////////////////////////////////////////////////////////

const router = express.Router();

/////////////////////////////////////////////////////////////////////////////////////////////


router.route('/')
    .get(personalTourController.getAllPersonalTours)
    .post(personalTourController.createNewPersonalTour);

router.route('/:userid')
    .get(personalTourController.getPersonalTour)
    .patch(personalTourController.updatePersonalTour)
    .delete(personalTourController.deletePersonalTour);

/////////////////////////////////////////////////////////////////////////////////////////////

module.exports = router;